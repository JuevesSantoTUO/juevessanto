// --- FUNCIONES DEL MODAL ---
function abrirItinerario(año) {
    const modal = document.getElementById('modal-itinerario');
    const info = document.getElementById('info-itinerario');
    
    const data = datosItinerarios[año];
    
    if (data) {
        info.innerHTML = `
            <div class="modal-header-info">
                <h2>${data.titulo}</h2>
            </div>
            <div class="modal-body-info">
                ${data.contenido}
            </div>
        `;
        modal.style.display = "block";
    }
}

function cerrarModal() {
    document.getElementById('modal-itinerario').style.display = "none";
}

// Cerrar si el usuario hace clic fuera de la caja blanca
window.onclick = function(event) {
    const modal = document.getElementById('modal-itinerario');
    if (event.target == modal) {
        cerrarModal();
    }
}